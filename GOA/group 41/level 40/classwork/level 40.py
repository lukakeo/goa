def basic_op(operator, value1, value2):
    if operator == "+":
        return value1 + value2
    elif operator == "-":
        return value1 - value2
    elif operator == "*":
        return value1 * value2
    elif operator == "**":
        return value1 ** value2
    elif operator == "/":
        return value1 / value2
    elif operator == "//":
        return value1 // value2
    else:
        print("error")
    basic_op == value1 + operator + value2

